--- Generate Monthly Sales According to the input
CREATE PROCEDURE getMonthlyDetails (IN FromDate DATE, ToDate DATE) BEGIN DECLARE c cursor with return for SELECT ORDER_DATE, PAYMENT_ID, ORDER.ORDER_ID, ORDER.ORDER_PRICE, ORDER.ORDER_FINALPRICE, ORDER.ORDER_DELIVERYFEE, PAYMENT_AMOUNT FROM ORDER, PAYMENT WHERE (PAYMENT.ORDER_ID = ORDER.ORDER_ID) AND (ORDER.ORDER_DATE BETWEEN FromDate AND ToDate) ORDER BY (ORDER.ORDER_DATE); OPEN c; END;
CALL getMonthlyDetails('2022-02-01','2022-02-28');

--- Shows the restaurant that sells the food
CREATE PROCEDURE DisplayRestaurantWithThisFood (IN FOODNAME VARCHAR(30)) BEGIN DECLARE c cursor with return for SELECT RESTAURANT.RESTAURANT_ID, RESTAURANT_NAME, FOOD_ID, FOOD_NAME, FOOD_PRICE, FOOD_DESCRIPTION FROM RESTAURANT, FOOD WHERE FOODNAME = FOOD_NAME AND FOOD.RESTAURANT_ID = RESTAURANT.RESTAURANT_ID ORDER BY RESTAURANT.RESTAURANT_ID; OPEN c; END;
